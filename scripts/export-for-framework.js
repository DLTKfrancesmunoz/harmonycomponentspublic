#!/usr/bin/env node
/**
 * Optional: Export Harmony design system overview and (optionally) component build specs
 * to a framework-friendly format (e.g. CSS variables for shadcn or similar).
 *
 * Usage:
 *   node scripts/export-for-framework.js                    # Emit CSS vars from design-system-overview
 *   node scripts/export-for-framework.js --format=json      # Emit JSON bundle
 *   node scripts/export-for-framework.js --component=Button # Include Button primary-cp-light-md spec
 *
 * Output: Writes to mcp-data/exports/ (create if needed).
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const OVERVIEW_PATH = path.join(ROOT, 'mcp-data', 'design-system-overview.json');
const COMPONENTS_DIR = path.join(ROOT, 'mcp-data', 'components');
const OUT_DIR = path.join(ROOT, 'mcp-data', 'exports');

const args = process.argv.slice(2);
const format = args.find((a) => a.startsWith('--format='))?.split('=')[1] || 'css';
const componentName = args.find((a) => a.startsWith('--component='))?.split('=')[1];

function loadOverview() {
  const raw = fs.readFileSync(OVERVIEW_PATH, 'utf-8');
  return JSON.parse(raw);
}

function loadComponentSpec(componentName, key = 'primary-cp-light-md') {
  const filePath = path.join(COMPONENTS_DIR, `${componentName.toLowerCase()}.json`);
  if (!fs.existsSync(filePath)) return null;
  const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));
  return data.buildSpecs?.[key] || null;
}

function emitCssVars(overview) {
  const lines = [
    '/* Harmony Design System — exported for framework (e.g. shadcn) */',
    '/* Generated by scripts/export-for-framework.js */',
    '',
    ':root {',
    `  /* Fonts */`,
    `  --harmony-font-sans: ${overview.fonts?.sans?.stack ?? "'Figtree', sans-serif"};`,
    `  --harmony-font-display: ${overview.fonts?.display?.stack ?? "'Lexend', sans-serif"};`,
    `  --harmony-font-mono: ${overview.fonts?.mono?.stack ?? "'JetBrains Mono', monospace"};`,
    '',
    `  /* Spacing (4px grid) */`,
  ];
  const scale = overview.spacing?.scale || {};
  for (const [k, v] of Object.entries(scale)) {
    lines.push(`  --harmony-space-${k}: ${v};`);
  }
  lines.push('', '  /* Icon fallback */');
  const fb = overview.fallbackIcons?.style || {};
  lines.push(`  --harmony-icon-fallback-bg: ${fb.background ?? '#ffecef'};`);
  lines.push(`  --harmony-icon-fallback-text: ${fb.color ?? '#c1253a'};`);
  lines.push(`  --harmony-icon-fallback-font-size: ${fb.fontSize ?? '8px'};`);
  lines.push(`  --harmony-icon-fallback-radius: ${fb.borderRadius ?? '2px'};`);
  lines.push('}');
  return lines.join('\n');
}

function emitJson(overview, componentSpec) {
  const out = {
    version: '1.0.0',
    source: 'Harmony Design System',
    designSystemOverview: overview,
    sampleBuildSpec: componentSpec || null,
  };
  return JSON.stringify(out, null, 2);
}

function main() {
  if (!fs.existsSync(OVERVIEW_PATH)) {
    console.error('design-system-overview.json not found. Run from repo root.');
    process.exit(1);
  }

  const overview = loadOverview();
  let componentSpec = null;
  if (componentName) {
    componentSpec = loadComponentSpec(componentName);
    if (componentSpec) console.log(`Including ${componentName} primary-cp-light-md build spec.`);
  }

  if (!fs.existsSync(OUT_DIR)) fs.mkdirSync(OUT_DIR, { recursive: true });

  if (format === 'css') {
    const css = emitCssVars(overview);
    const outPath = path.join(OUT_DIR, 'harmony-variables.css');
    fs.writeFileSync(outPath, css, 'utf-8');
    console.log(`Wrote ${outPath}`);
  } else {
    const json = emitJson(overview, componentSpec);
    const outPath = path.join(OUT_DIR, 'harmony-export.json');
    fs.writeFileSync(outPath, json, 'utf-8');
    console.log(`Wrote ${outPath}`);
  }
}

main();
